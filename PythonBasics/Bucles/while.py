x = 0 #Inicializamos x en 0
while x < 5: #Mientras x sea menor que 5
    print("Not there yet, x=" + str(x)) #Imprime el texto + el valor de str en esa iteracion
    x = x + 1 #En cada iteracion suma 1 al valor de x
    print("x=" + str(x)) #Nos imprime el nuevo valor de x, cuando el valor de x sea 5 la iteracion acabara